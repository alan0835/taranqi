// 合并所有新闻数据
export const allNews: NewsItem[] = [...campusNews, ...announcements, ...mediaFocus];

// 获取最新新闻（用于首页展示）
export const getLatestNews = (count: number = 3): NewsItem[] => {
  return [...allNews].sort(
    (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
  ).slice(0, count);
};

// 根据分类获取新闻
export const getNewsByCategory = (category: NewsCategory): NewsItem[] => {
  return allNews.filter(news => news.category === category);
};

// 根据slug获取单个新闻详情
export const getNewsBySlug = (slug: string): NewsItem | undefined => {
  return allNews.find(news => news.slug === slug);
}; 